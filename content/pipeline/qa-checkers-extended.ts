const DEPRECATED_MODULES = [
  "mass_endomorphism",
  "LAMBDA_PROTON_DOC",
  "LAMBDA_NEUTRON_DOC",
  "LAMBDA_PROTON_FITTED",
  "LAMBDA_NEUTRON_FITTED",
];

export function checkCanonicalScriptNotDeprecated(
  tsPath: string | undefined,
): CheckerResult {
  const ts = readMaybe(tsPath);
  if (!ts) return { result: "pass", hits: [] };
  const { probe } = getComputationField(ts);
  const witness = getComputationField(ts).witness;
  const scriptMatch = ts.match(/script:\s*["']([^"']+)["']/);
  if (!scriptMatch) return { result: "pass", hits: [] };
  const scriptRelPath = scriptMatch[1];
  const scriptAbsPath = resolve(REPO_ROOT, scriptRelPath);
  if (!existsSync(scriptAbsPath)) return { result: "pass", hits: [] };
  const scriptSrc = readFileSync(scriptAbsPath, "utf-8");
  const hits: CheckerHit[] = [];
  // Check for script-level deprecation markers (first 10 lines or
  // module docstring). Ignore DEPRECATED inside function-level
  // docstrings deeper in the file (e.g. hecke_core.py has
  // [DEPRECATED] on individual internal functions, not the module).
  const firstLines = scriptSrc.split("\n").slice(0, 10).join("\n");
  const moduleDocstring = scriptSrc.match(/^("""[\s\S]*?"""|'''[\s\S]*?''')/m)?.[0] ?? "";
  const headerZone = firstLines + "\n" + moduleDocstring;
  if (/\bSUPERSEDED\b/i.test(headerZone) || /\bDEPRECATED\b/i.test(headerZone)) {
    hits.push({
      file: scriptRelPath,
      line: 1,
      text: "computation script contains SUPERSEDED/DEPRECATED marker",
    });
  }
  for (const mod of DEPRECATED_MODULES) {
    // Only match actual imports, not comment mentions.
    // Python: `from mass_endomorphism import ...` or `import mass_endomorphism`
    const importRe = new RegExp(
      `^\\s*(?:from\\s+${mod}\\s+import|import\\s+${mod})`, "m",
    );
    if (importRe.test(scriptSrc)) {
      hits.push({
        file: scriptRelPath,
        line: 1,
        text: `computation script imports deprecated module: ${mod}`,
      });
    }
  }
  return { result: hits.length > 0 ? "fail" : "pass", hits };
}

