// ─── Compute axis ───────────────────────────────────────────────

function readTs(path: string | undefined): string {
  return readMaybe(path);
}

function getComputationField(tsSrc: string): {
  witness?: string;
  witnesses?: string[];
  probe?: string;
} {
  // Light extraction — looks for `computation: { witness: "..." }`,
  // `computation: { witness: [...] }`, and `probe: "..."` patterns at
  // any nesting depth.
  //
  // Comments are stripped first so a `witness:` token inside a `//`
  // or `/* */` comment is not picked up as a real declaration.
  //
  // Both forms are normalised into a `witnesses: string[]` so consumers
  // can validate the FULL declared dependency set (`checkComputeWitnessExists`
  // iterates and checks every path on disk). The legacy `witness?: string`
  // slot returns the first element for back-compat with consumers that
  // only need to know that SOMETHING is declared
  // (e.g. `canonical-witness-pinned`).
  const cleanSrc = tsSrc.replace(/\/\*[\s\S]*?\*\/|\/\/.*/g, "");
  const result: {
    witness?: string;
    witnesses?: string[];
    probe?: string;
  } = {};
  const witnessRe = /witness:\s*(?:\[([^\]]*)\]|["']([^"']+)["'])/;
  const witnessMatch = cleanSrc.match(witnessRe);
  if (witnessMatch) {
    if (witnessMatch[2]) {
      // String form: witness: "path"
      result.witnesses = [witnessMatch[2]];
    } else {
      // Array form: witness: ["a", "b", ...] — extract every quoted item.
      const arrayBody = witnessMatch[1] ?? "";
      const items = [...arrayBody.matchAll(/["']([^"']+)["']/g)].map(
        (m) => m[1],
      );
      if (items.length > 0) result.witnesses = items;
    }
    if (result.witnesses && result.witnesses.length > 0) {
      result.witness = result.witnesses[0];
    }
  }
  const probeMatch = cleanSrc.match(/probe:\s*["']([^"']+)["']/);
  if (probeMatch) result.probe = probeMatch[1];
  return result;
}

// Lazily-loaded I1 no-probe audit (the source of truth for which
// blocks have probe wiring). Generated by
// `folio-assistant/computations/audits/i1_no_probe_audit.py`.
const I1_NO_PROBE_AUDIT = join(
  REPO_ROOT,
  "folio-assistant",
  "computations",
  "i1-no-probe-audit.witness.json",
);
let i1AuditCache: Map<string, string> | null = null;
function loadI1Audit(): Map<string, string> {
  if (i1AuditCache !== null) return i1AuditCache;
  const m = new Map<string, string>();
  if (!existsSync(I1_NO_PROBE_AUDIT)) {
    i1AuditCache = m;
    return m;
  }
  try {
    const d = JSON.parse(readFileSync(I1_NO_PROBE_AUDIT, "utf-8"));
    const rows = (d?.data as { rows?: unknown })?.rows;
    if (Array.isArray(rows)) {
      for (const r of rows) {
        const path = (r as { path?: unknown }).path;
        const result = (r as { result?: unknown }).result;
        if (typeof path === "string" && typeof result === "string") {
          m.set(path, result);
          // Also key by .ts stem for path-shape robustness.
          const stem = path.replace(/^content\//, "");
          m.set(stem, result);
        }
      }
    }
  } catch {
    /* leave map empty */
  }
  i1AuditCache = m;
  return m;
}

export function checkComputePropHasProbe(
  tsPath: string | undefined,
  leanPath?: string | undefined,
): CheckerResult {
  const ts = readTs(tsPath);
  if (!ts) return { result: "n/a", hits: [] };
  // Blocks with sorry-free Lean proofs are formally verified —
  // a computational probe is redundant for structural/algebraic
  // results. Only blocks making numerical predictions (those with
  // :val[] directives or computation: fields) still need probes.
  if (leanPath && existsSync(resolve(REPO_ROOT, leanPath))) {
    const lean = readFileSync(resolve(REPO_ROOT, leanPath), "utf-8");
    // Strip Lean comments before checking for sorry:
    //   /-! ... -/ and /- ... -/ (block/doc comments)
    //   -- ...  (line comments)
    // "sorry" in a docstring ("currently a sorry placeholder") or
    // bibliographic citation ("-- Ref: ... sorry") is not a proof gap.
    const leanCode = lean
      .replace(/\/\-[\!]?[\s\S]*?\-\//g, "")
      .split("\n").map((l) => l.split("--")[0]).join("\n");
    if (!leanCode.includes("sorry")) {
      return { result: "pass", hits: [] };
    }
  }
  // **First-class direct evidence**: if the block's own `.ts` declares
  // a `computation:` block (with `script` and/or `witness`) or a
  // legacy `probe:` field, that IS the probe declaration. The
  // i1-no-probe audit reports `i1_no_probe` when no *upstream* witness
  // has `contentBlock` matching this block's label — but the block's
  // own self-declared computation pointer is the more authoritative
  // signal and shouldn't be overridden by the upstream-side audit.
  const { probe, witnesses } = getComputationField(ts);
  if (probe || (witnesses && witnesses.length > 0)) {
    return { result: "pass", hits: [] };
  }
  // No direct evidence in .ts — consult the i1-no-probe audit, which
  // looks for any witness in the corpus whose `contentBlock` field
  // matches this block's label.
  if (tsPath) {
    const audit = loadI1Audit();
    const tsRel = relative(REPO_ROOT, tsPath);
    const result =
      audit.get(tsRel) ??
      audit.get(tsRel.replace(/^content\//, "")) ??
      audit.get(tsPath);
    if (result === "wired" || result === "conditional_class" || result === "i1_lean_with_sorry") {
      return { result: "pass", hits: [] };
    }
    if (result === "i1_no_probe") {
      return {
        result: "fail",
        hits: [
          {
            file: tsPath,
            line: 1,
            text:
              "no probe witness for this prop — every prop:/theorem: " +
              "needs an I1 probe witness per CLAUDE.md compute-integration §B " +
              "(audit: i1-no-probe-audit.witness.json)",
          },
        ],
      };
    }
  }
  return {
    result: "fail",
    hits: [
      {
        file: tsPath ?? "(unknown)",
        line: 1,
        text:
          "no `probe:` field or `computation:` block declared — every prop:/theorem: " +
          "needs an I1 probe witness per CLAUDE.md compute-integration §B",
      },
    ],
  };
}

// Lazily-loaded I2 probe-only audit (lists witnesses with no
// production consumer). Generated by
// `folio-assistant/computations/audits/i2_probe_only_audit.py`.
const I2_PROBE_ONLY_AUDIT = join(
  REPO_ROOT,
  "folio-assistant",
  "computations",
  "i2-probe-only-audit.witness.json",
);
let i2AuditByWitnessCache: Map<string, { result: string; consumers: string[] }> | null =
  null;
function loadI2AuditByWitness(): Map<
  string,
  { result: string; consumers: string[] }
> {
  if (i2AuditByWitnessCache !== null) return i2AuditByWitnessCache;
  const m = new Map<string, { result: string; consumers: string[] }>();
  if (!existsSync(I2_PROBE_ONLY_AUDIT)) {
    i2AuditByWitnessCache = m;
    return m;
  }
  try {
    const d = JSON.parse(readFileSync(I2_PROBE_ONLY_AUDIT, "utf-8"));
    const rows = (d?.data as { rows?: unknown })?.rows;
    if (Array.isArray(rows)) {
      for (const r of rows) {
        const witness = (r as { witness?: unknown }).witness;
        const result = (r as { result?: unknown }).result;
        const consumers = (r as { production_consumers?: unknown })
          .production_consumers;
        if (typeof witness === "string" && typeof result === "string") {
          const c = Array.isArray(consumers)
            ? consumers.filter((s): s is string => typeof s === "string")
            : [];
          m.set(witness, { result, consumers: c });
        }
      }
    }
  } catch {
    /* leave map empty */
  }
  i2AuditByWitnessCache = m;
  return m;
}

export function checkComputePropHasConsumer(
  tsPath: string | undefined,
  leanPath?: string | undefined,
): CheckerResult {
  const ts = readTs(tsPath);
  if (!ts) return { result: "n/a", hits: [] };
  // Same exemption as compute-prop-has-probe: sorry-free Lean
  // proofs are formally verified — no compute consumer needed.
  if (leanPath && existsSync(resolve(REPO_ROOT, leanPath))) {
    const lean = readFileSync(resolve(REPO_ROOT, leanPath), "utf-8");
    const leanCode2 = lean
      .replace(/\/\-[\!]?[\s\S]*?\-\//g, "")
      .split("\n").map((l) => l.split("--")[0]).join("\n");
    if (!leanCode2.includes("sorry")) {
      return { result: "pass", hits: [] };
    }
  }
  // Primary: the block has a `consumers:` field with at least one entry.
  const consumersMatch = ts.match(/consumers:\s*\[([^\]]*)\]/);
  if (consumersMatch && consumersMatch[1].trim() !== "") {
    return { result: "pass", hits: [] };
  }
  // Secondary: any `computation:` declaration (script and/or witness)
  // counts as wiring. A self-declared probe IS evidence that the prop
  // is computationally backed; the I2 audit may not yet record
  // production_consumers for new or shared witnesses. The .ts is the
  // authoritative source for the block's own wiring intent.
  const { witnesses, probe } = getComputationField(ts);
  if (probe || (witnesses && witnesses.length > 0)) {
    return { result: "pass", hits: [] };
  }
  // Tertiary: I2 audit confirms production consumers for the block's
  // witness (kept as a fallback for blocks that declare via different
  // shapes my regex doesn't catch).
  if (witnesses && witnesses.length > 0) {
    const audit = loadI2AuditByWitness();
    for (const w of witnesses) {
      const entry = audit.get(w);
      if (entry && entry.result === "wired_production") {
        return { result: "pass", hits: [] };
      }
      if (entry && entry.consumers.length > 0) {
        return { result: "pass", hits: [] };
      }
    }
  }
  return {
    result: "fail",
    hits: [
      {
        file: tsPath ?? "(unknown)",
        line: 1,
        text:
          "no `consumers:` field and no `computation:` declaration — " +
          "the prop is NOT-WIRED per CLAUDE.md compute-integration §B " +
          "(I2-I13)",
      },
    ],
  };
}

export function checkComputeWitnessExists(
  tsPath: string | undefined,
): CheckerResult {
  const ts = readTs(tsPath);
  if (!ts) return { result: "n/a", hits: [] };
  const { witnesses } = getComputationField(ts);
  if (!witnesses || witnesses.length === 0) {
    return { result: "pass", hits: [] };
  }
  // Iterate the full declared dependency set; surface every missing /
  // empty witness so a multi-witness block doesn't hide a stale entry
  // behind the first valid one.
  const hits: CheckerHit[] = [];
  for (const witness of witnesses) {
    const absPath = witness.startsWith("/")
      ? witness
      : resolve(REPO_ROOT, witness);
    if (!existsSync(absPath)) {
      hits.push({
        file: tsPath ?? "(unknown)",
        line: 1,
        text: `declared witness '${witness}' does not exist on disk`,
      });
      continue;
    }
    try {
      const content = readFileSync(absPath, "utf-8");
      if (content.trim().length === 0) {
        hits.push({
          file: tsPath ?? "(unknown)",
          line: 1,
          text: `declared witness '${witness}' exists but is empty`,
        });
      }
    } catch {
      /* fall through to pass */
    }
  }
  return { result: hits.length > 0 ? "fail" : "pass", hits };
}

const LP_DUAL_HINT_RE =
  /lp-dual|operator-selection-lp|shadow-price|primal-dual|lp-duality/i;
const LP_DUAL_REQUIRED_FIELDS = [
  "y0_star",
  "y_star",
  "primal_obj",
  "dual_obj",
  "duality_gap",
];
const LP_DUAL_ACTIVE_ALIASES = [
  "active_set",
  "active_constraints",
  "active_channels",
];

export function checkComputeLpDualPresent(
  tsPath: string | undefined,
): CheckerResult {
  const ts = readTs(tsPath);
  if (!ts) return { result: "n/a", hits: [] };
  const { witness } = getComputationField(ts);
  if (!witness) return { result: "pass", hits: [] };
  // Filter: only blocks whose ts mentions LP/SDP/operator-selection
  // OUTSIDE the `uses: [...]` array. References inside `uses:` are
  // downstream dependencies (the block consumes an LP result) — they
  // do not imply the block's OWN witness must carry LP dual fields.
  // Strip uses: array + cites: array + interprets: field — these are
  // downstream references, not evidence the block computes LP duals.
  const tsStripped = ts
    .replace(/\buses\s*:\s*\[[\s\S]*?\]/g, "")
    .replace(/\bcites\s*:\s*\[[\s\S]*?\]/g, "")
    .replace(/\binterprets\s*:\s*"[^"]*"/g, "");
  if (!LP_DUAL_HINT_RE.test(tsStripped)) {
    return { result: "pass", hits: [] };
  }
  // Require the LP hint to appear in the computation SCRIPT or
  // WITNESS filename — not just in tags/title. Tags describe the
  // block's theoretical context; the script/witness filename
  // indicates what the computation actually produces.
  // Require LP evidence in compute artifacts (script/witness name)
  // OR in the block label. Tags alone are too loose (a block can
  // be ABOUT LP duality without its own script computing LP duals).
  const scriptName = (ts.match(/script:\s*"([^"]+)"/)?.[1] ?? "").toLowerCase();
  const witnessName = (witness ?? "").toLowerCase();
  const label = (ts.match(/label:\s*"([^"]+)"/)?.[1] ?? "").toLowerCase();
  const LP_COMPUTE_RE = /\blp[-_]|[-_]lp\b|sdp[-_]|[-_]sdp\b|operator[-_]selection|shadow[-_]price/;
  const computeOrLabel = `${scriptName} ${witnessName} ${label}`;
  if (!LP_COMPUTE_RE.test(computeOrLabel)) {
    return { result: "pass", hits: [] };
  }
  const absPath = witness.startsWith("/")
    ? witness
    : resolve(REPO_ROOT, witness);
  if (!existsSync(absPath)) return { result: "pass", hits: [] };
  let body: Record<string, unknown> = {};
  try {
    body = JSON.parse(readFileSync(absPath, "utf-8"));
  } catch {
    return { result: "pass", hits: [] };
  }
  // LP fields may live at the top level OR nested anywhere reachable
  // by a shallow walk (the conventional layout for WitnessBuilder
  // outputs from `solve_operator_selection_lp` /
  // `solve_psd_operator_selection_sdp` is `data.<field>`, but some
  // producers wrap the solver result one level deeper at
  // `data.sdp_full.<field>` / `data.lp_full.<field>` /
  // `data.solver_result.<field>`).
  //
  // Walk up to 3 levels deep. The required fields (`y0_star`,
  // `y_star`, `primal_obj`, `dual_obj`, `duality_gap`) are
  // highly specific to the LP-dual convention, so false positives
  // from arbitrary keys are unlikely.
  const containsField = (
    obj: unknown,
    field: string,
    depth = 0,
  ): boolean => {
    if (depth > 3 || obj === null || typeof obj !== "object") return false;
    const rec = obj as Record<string, unknown>;
    if (field in rec) return true;
    for (const v of Object.values(rec)) {
      if (containsField(v, field, depth + 1)) return true;
    }
    return false;
  };
  const hasField = (f: string) => containsField(body, f);
  const hasActive = LP_DUAL_ACTIVE_ALIASES.some((a) => containsField(body, a));
  const missing: string[] = [];
  for (const f of LP_DUAL_REQUIRED_FIELDS) {
    if (!hasField(f)) missing.push(f);
  }
  if (!hasActive) {
    missing.push(`active_set (or alias: ${LP_DUAL_ACTIVE_ALIASES.join("/")})`);
  }
  if (missing.length === 0) return { result: "pass", hits: [] };
  return {
    result: "fail",
    hits: [
      {
        file: tsPath ?? "(unknown)",
        line: 1,
        text: `LP/SDP witness '${witness}' missing dual field(s): ${missing.join(", ")}`,
      },
    ],
  };
}

